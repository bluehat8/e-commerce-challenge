<script>
    function showAdminKeyInput(event) {
        const adminKeyField = document.getElementById('admin_key_field');
        if (event.target.value === 'admin') {
            adminKeyField.style.display = 'block';
        } else {
            adminKeyField.style.display = 'none';
        }
    }
</script>
